SOCRATIC_STRATEGIES = {
    "wrong_answer": [
        "讓我們一起看看你的思路。你為什麼這樣想？",
        "如果換一個角度來看這道題，你會怎麼想？",
        "你的答案很接近了！再檢查一下哪個步驟？",
    ],
    "correct_answer": [
        "非常好！你能解釋一下你是怎麼得出這個答案的嗎？",
        "答對了！試一試更難的變化題？",
        "完全正確！你能教我這道題是怎麼做的嗎？",
    ],
    "stuck": [
        "不要急，先看看題目給了你什麼條件？",
        "我們一步一步來。第一步你覺得應該做什麼？",
        "試著畫一張圖來幫助理解？",
    ],
    "misconception_detected": [
        "我注意到你可能混淆了一個概念。你聽過___嗎？",
        "這是很常見的錯誤！讓我們重新認識___的概念。",
        "你對___的理解有一點偏差。我們一起理清它。",
    ],
}

def get_topic_knowledge(topic: str) -> dict:
    """獲取課題完整知識庫（支援模糊匹配）"""
    if topic in TOPIC_KNOWLEDGE:
        return TOPIC_KNOWLEDGE[topic]
    # 模糊匹配
    for key in TOPIC_KNOWLEDGE:
        if topic in key or key in topic:
            return TOPIC_KNOWLEDGE[key]
    # 返回通用知識
    return {
        "grade": "P3-P6", "domain": "通用",
        "key_concepts": [topic],
        "common_mistakes": ["概念理解不清", "計算錯誤"],
        "socratic": [f"關於{topic}，你已經知道什麼？", f"{topic}的關鍵概念是什麼？", f"試舉一個{topic}的例子？"]
    }

def get_socratic_strategy(situation: str) -> str:
    """根據情況返回合適的引導策略"""
    strategies = SOCRATIC_STRATEGIES.get(situation, SOCRATIC_STRATEGIES["stuck"])
    return random.choice(strategies)

def get_grade_topics(grade: str) -> list:
    """獲取指定年級的所有課題"""
    return GRADE_TOPICS.get(grade, [])

def get_all_topics() -> list:
    """獲取所有課題列表"""
    return list(TOPIC_KNOWLEDGE.keys())

def get_topics_by_domain(domain: str) -> list:
    """按領域獲取課題"""
    return [t for t, k in TOPIC_KNOWLEDGE.items() if k["domain"] == domain]